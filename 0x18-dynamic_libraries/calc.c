#include "calc.h"

/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the sum of the two numbers
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the diffrence of the two numbers
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the multiplication of the two numbers
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - mod two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the reminder of the two numbers
*/

int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - div two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the division of the two numbers
*/

int div(int a, int b)
{
	return (a / b);
}
